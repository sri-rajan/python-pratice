import turtle
a=turtle.Turtle()
a.fillcolor("red")
a.begin_fill()
a.forward(100)
a.goto(100,100)
a.goto(0,100)
a.goto(0,0)
a.end_fill()
a.fillcolor("green")
a.begin_fill()
a.goto(-50,0)
a.goto(-50,-100)
a.goto(150,-100)
a.goto(150,0)
a.goto(0,0)
a.end_fill()
a.up()
a.goto(0,120)
a.down()
a.fillcolor("blue")
a.begin_fill()
a.forward(100)
a.left(90)
a.forward(30)
a.left(90)
a.forward(100)
a.left(90)
a.forward(30)
a.end_fill()

